function config = defaultTeachingGeneratorConfig()
%DEFAULTTEACHINGGENERATORCONFIG Параметры генератора учебного набора данных.
% Русское название: Конфигурация генератора учебных данных.
%
% Назначение:
% Эта функция не генерирует данные напрямую. Она только собирает все
% управляемые параметры генератора в одну структуру config:
% - имена исходных файлов;
% - имя выходного каталога;
% - даты начала учебных сегментов;
% - параметры использования остаточной составляющей;
% - список искусственно вносимых учебных аномалий.
%
% Такой подход позволяет менять сценарий генерации данных без изменения
% основной логики скрипта generate_teaching_dataset.m.

config = struct();

%% Общие параметры генерации
% randomSeed фиксирует начальное состояние генератора случайных чисел.
% Это нужно для воспроизводимости результатов, если в дальнейшем в
% алгоритме будут использоваться случайные операции.
config.randomSeed = 20260702;

% sourceDataset используется как текстовая метка в итоговой таблице.
% Она показывает, на каком реальном наборе данных основана учебная выборка.
config.sourceDataset = "200_stab";

%% Входные и выходные параметры
% Здесь задаются только имена файлов, а не полный абсолютный путь.
% Основной скрипт сам ищет эти файлы в ожидаемых каталогах проекта.
config.input = struct();
config.input.magFileName = "200_magn_stab.txt";
config.input.navFileName = "200_lla_stab.csv";

% Корневой каталог, куда генератор будет сохранять итоговую учебную выборку,
% raw-like файлы, описание аномалий, метаданные и проверочные рисунки.
config.output = struct();
config.output.rootDirName = "Задание 2 · Данные для обучения";

%% Временные сегменты учебного набора
% Каждый элемент segmentStarts задает начало отдельного учебного фрагмента.
% Внутри каждого фрагмента сохраняются относительные интервалы времени из
% исходного навигационного набора, но весь фрагмент переносится на новую дату.
%
% Количество строк в итоговом наборе данных примерно равно:
% height(nav) * numel(config.segmentStarts).
%
% Чтобы изменить покрываемый временной интервал, достаточно изменить этот
% массив дат. Основной алгоритм при этом менять не нужно.
config.segmentStarts = [
    datetime(2026, 1, 12, 9, 0, 0, "TimeZone", "UTC")
    datetime(2026, 1, 18, 11, 30, 0, "TimeZone", "UTC")
    datetime(2026, 1, 25, 15, 0, 0, "TimeZone", "UTC")
    datetime(2026, 2, 10, 8, 15, 0, "TimeZone", "UTC")
    datetime(2026, 3, 7, 10, 0, 0, "TimeZone", "UTC")
    datetime(2026, 3, 21, 17, 45, 0, "TimeZone", "UTC")
    datetime(2026, 4, 13, 12, 0, 0, "TimeZone", "UTC")
    datetime(2026, 5, 4, 7, 30, 0, "TimeZone", "UTC")
    datetime(2026, 5, 11, 14, 20, 0, "TimeZone", "UTC")
    datetime(2026, 5, 19, 19, 10, 0, "TimeZone", "UTC")
    datetime(2026, 5, 27, 5, 50, 0, "TimeZone", "UTC")
    datetime(2026, 6, 16, 16, 40, 0, "TimeZone", "UTC")
    ];

% Искусственное смещение долготы между соседними сегментами.
% Оно нужно, чтобы повторно используемый навигационный фрагмент не ложился
% каждый раз на одну и ту же трассу на карте.
% Важно: это не орбитальное прогнозирование, а учебная трансформация.
config.longitudeShiftPerSegmentDeg = 17.5;

%% Параметры остаточной составляющей
% Остаток показывает отличие реального измеренного модуля магнитного поля от
% теоретического значения по IGRF. При генерации учебных данных этот остаток
% добавляется к новому теоретическому полю, чтобы сохранить характер реальных
% измерений: шумы, смещения и небольшие локальные отклонения.
config.residual = struct();

% Множитель для робастной фильтрации остатка через median и MAD.
% Чем меньше значение, тем строже отбор "нормальных" остатков.
config.residual.robustSigmaMultiplier = 4;

% Режим повторного использования остатка.
% "cyclic" означает циклическое переиспользование последовательности остатков.
config.residual.mode = "cyclic";

% Смещение начала остаточной последовательности для каждого следующего сегмента.
% Это предотвращает полное повторение одного и того же остаточного профиля
% во всех сгенерированных фрагментах.
config.residual.shiftStep = 97;

% scale управляет амплитудой остатка:
% 0.5 — остаток слабее, данные более гладкие;
% 1.0 — исходная амплитуда;
% 2.0 — остаток сильнее, данные более "шумные".
config.residual.scale = 1;

% biasMGs добавляет постоянное смещение к остатку, если нужно смоделировать
% систематическое отклонение магнитометрического сигнала.
config.residual.biasMGs = 0;

% clipEnabled и clipMGs позволяют ограничить слишком большие остатки.
% В текущей конфигурации ограничение выключено, но границы заданы заранее.
config.residual.clipEnabled = false;
config.residual.clipMGs = [-300 300];

%% Параметры искусственных аномалий
% Если injectAnomalies = false, генератор должен сформировать только
% нормальный полусинтетический набор без искусственно внесенных ошибок.
config.injectAnomalies = true;

% Массив аномалий задается в конфигурации, чтобы можно было:
% - включать и отключать отдельные аномалии;
% - менять их положение внутри сегмента;
% - менять амплитуду скачков;
% - добавлять новые учебные случаи без переписывания основной функции.
config.anomalies = repmat(defaultAnomaly(), 8, 1);

% A001: скачок высоты. Проверяет способность обнаруживать ошибки в
% навигационных данных по графику высоты и непрерывности орбитального фрагмента.
config.anomalies(1) = makeAnomaly(true, "A001", "altitude_jump", "segment_2", 120, 132, "easy", ...
    struct("deltaKm", 65));

% A002: скачок координат. На карте должен выглядеть как разрыв или резкое
% смещение трассы спутника.
config.anomalies(2) = makeAnomaly(true, "A002", "latlon_jump", "segment_4", 260, 270, "easy", ...
    struct("deltaLat", 42, "deltaLon", -95));

% A003: кратковременный пик магнитного поля.
% Это простая для обнаружения аномалия на графике |B|(t).
config.anomalies(3) = makeAnomaly(true, "A003", "magnetic_spike", "segment_5", 310, 314, "easy", ...
    struct("deltaMGs", 180));

% A004: локальное снижение магнитного поля.
% В отличие от пика, выглядит как провал относительно модели и соседних точек.
config.anomalies(4) = makeAnomaly(true, "A004", "magnetic_drop", "segment_7", 180, 205, "medium", ...
    struct("deltaMGs", -130));

% A005: ступенчатое смещение магнитного поля.
% Имитирует не отдельный выброс, а длительный сдвиг уровня измерений.
config.anomalies(5) = makeAnomaly(true, "A005", "magnetic_step", "segment_9", 420, 500, "medium", ...
    struct("deltaMGs", 55));

% A006: блок пропущенных значений.
% Используется для проверки обработки NaN при расчетах и построении графиков.
config.anomalies(6) = makeAnomaly(true, "A006", "nan_block", "segment_10", 90, 96, "hard", ...
    struct());

% A007: строки с нулевым магнитным полем.
% Имитирует недостоверные измерения, которые должны быть найдены при очистке.
config.anomalies(7) = makeAnomaly(true, "A007", "zero_magnetic_rows", "segment_11", 540, 545, "medium", ...
    struct());

% A008: поврежденная временная метка.
% Обычно соответствует time_ms = 0 или переходу к 1970-01-01 в UTC.
config.anomalies(8) = makeAnomaly(true, "A008", "zero_timestamp", "segment_12", 360, 362, "hard", ...
    struct());
end

function anomaly = defaultAnomaly()
%DEFAULTANOMALY Возвращает шаблон выключенной аномалии.
% Такой шаблон нужен, чтобы все элементы массива config.anomalies имели
% одинаковую структуру полей. Это упрощает дальнейшую обработку через цикл.
anomaly = struct( ...
    "enabled", false, ...
    "id", "", ...
    "type", "", ...
    "segmentId", "", ...
    "localStart", 1, ...
    "localEnd", 1, ...
    "severity", "medium", ...
    "params", struct());
end

function anomaly = makeAnomaly(enabled, id, type, segmentId, localStart, localEnd, severity, params)
%MAKEANOMALY Создает одну запись об учебной аномалии.
%
% Положение задается через segmentId и локальный диапазон строк внутри
% сегмента. Например, segment_5, 310:314 означает строки 310-314 внутри
% пятого сгенерированного фрагмента, а не глобальные строки всей таблицы.
%
% params содержит параметры конкретного типа аномалии:
% - deltaKm для скачка высоты;
% - deltaLat и deltaLon для скачка координат;
% - deltaMGs для магнитных пиков, провалов и ступенчатых смещений.
anomaly = defaultAnomaly();
anomaly.enabled = enabled;
anomaly.id = string(id);
anomaly.type = string(type);
anomaly.segmentId = string(segmentId);
anomaly.localStart = localStart;
anomaly.localEnd = localEnd;
anomaly.severity = string(severity);
anomaly.params = params;
end
